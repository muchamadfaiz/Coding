# operator dalam bentuk methods

## merubah case dari string

# merubah semua ke uppercase

salam = "bro"
print("normal =", salam)
salam = salam.upper()
print("upper =", salam)

# merubah semua ke lowercase
# bisa pake .lower
alay = "aKu keCe AbiezZZZ"
print("normal =", alay)
alay = alay.lower()
print("lower =", alay)

# bisa pake .casefold()
alay = "aKu keCe AbiezZZZ"
print("normal =", alay)
alay = alay.casefold()
print("lower =", alay)