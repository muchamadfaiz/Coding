# ELIF = ELSE IF Statement

nama = input("Nama kamu siapa? ")

if nama == "pawpaw": # kondisi 1
    print(f"{nama} kamu ganteng banget") #aksi true 1
elif nama == "faiz": # kondisi 2
    print(f"{nama} kamu juga ganteng banget") #aksi true 2
elif nama == "umiw": # kondisi 3
    print(f"{nama} mah cantik") #aksi true 2
else :
    print(f"siapa si {nama} ga kenal")
print("akhir dari program")
