#Concatenation
first = "Muchamad"
last = "Faiz"
full = first + " " + last #Concatenation
print(full)

#Formatted String
first = "Muchamad"
last = "Faiz"
full = f"{first} {last}"
print(full)

#we can put any value expression in between curly braces
first = "Muchamad"
last = "Faiz"
full = f"{len(first)} {2+2}"
print(full)