# For Loops
# Use loops to create repetition
for number in range(3):
    print("Attempt", number + 1, (number + 1) * "." )

# make it cleaner
for number in range(1, 4):
    print("Attempt", number, number * "." )

# with steps
for number in range(1, 10, 2):
    print("Attempt", number, number * "." )



